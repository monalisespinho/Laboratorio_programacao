public class Calculadora {
    int numberOne;
    int numberTwo;

    int somar(){
        int soma = numberOne + numberTwo;
        return soma;

    }
    int subtrair(){
        int subtrair = numberOne - numberTwo;
        return subtrair;
    }

    int multiplicar(){
        int multiplicar = numberOne * numberTwo;
        return multiplicar;
    }

    double dividir(){
        double dividir = numberOne/numberTwo;
        return dividir;
    }

}
